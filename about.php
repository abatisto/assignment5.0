<p id='bio'>The alternative punk powerhouse that is Cold Era got its start at the University of Vermont in 2015, 
    when Austin and Braden began to write songs late at night in an on-campus studio. Starting as just a 
    guitar and drums two-piece, Cold Era then recruited fellow UVM student Matt Curran to play bass. Later,
    they added Riley Kissenbirth to further solidify their full, dynamic sound. Cold Era has been described as 
    a unique alternative-punk outfit and they draw influence from bands such as Title Fight, The Story So Far,
    Basement, Balance and Composure, and Turnover. Cold Era began writing and recording their first EP in Spring
    2016 and they expect to release it and go on tour Summer 2016. </p>

